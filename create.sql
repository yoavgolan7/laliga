CREATE TABLE Team (
    teamID INT PRIMARY KEY,
    team_name VARCHAR(255) NOT NULL,
    points INT DEFAULT 0,
    wins INT DEFAULT 0,
    losses INT DEFAULT 0,
    matches_played INT DEFAULT 0,
    goals_for INT DEFAULT 0,
    goals_against INT DEFAULT 0,
    goal_difference INT DEFAULT 0,
    UNIQUE (team_name)  -- כל שם קבוצה חייב להיות ייחודי
);

CREATE TABLE Player (
    playerID INT PRIMARY KEY,
    teamID INT,
    player_name VARCHAR(255) NOT NULL,
    position VARCHAR(50),
    nationality VARCHAR(100),
    jersey_number INT,
    date_of_birth DATE,
    height INT,
    goals INT DEFAULT 0,
    assists INT DEFAULT 0,
    yellow_cards INT DEFAULT 0,
    red_cards INT DEFAULT 0,
    FOREIGN KEY (teamID) REFERENCES Team(teamID) ON DELETE SET NULL  -- אם קבוצה נמחקת, מזהה הקבוצה אצל השחקנים שלה יקבל ערך NULL
);

CREATE TABLE Game (
    gameID INT PRIMARY KEY,
    home_team_ID INT,
    away_team_ID INT,
    home_goals_scored INT,
    away_goals_scored INT,
    date DATE NOT NULL,
    FOREIGN KEY (home_team_ID) REFERENCES Team(teamID) ON DELETE CASCADE,  -- אם קבוצה ביתית נמחקת, המשחקים שלה יימחקו גם כן
    FOREIGN KEY (away_team_ID) REFERENCES Team(teamID) ON DELETE CASCADE,  -- אם קבוצה אורחת נמחקת, המשחקים שלה יימחקו גם כן
    CHECK (home_team_ID <> away_team_ID)  -- קבוצה לא יכולה לשחק מול עצמה במשחק
);

CREATE TABLE Event (
    eventID INT PRIMARY KEY,
    gameID INT,
    teamID INT,
    playerID INT,
    event_type VARCHAR(50) NOT NULL,
    event_time INT NOT NULL,
    FOREIGN KEY (gameID) REFERENCES Game(gameID) ON DELETE CASCADE,  -- אם משחק נמחק, האירועים שלו יימחקו גם כן
    FOREIGN KEY (teamID) REFERENCES Team(teamID) ON DELETE CASCADE,  -- אם קבוצה נמחקת, האירועים שלה יימחקו גם כן
    FOREIGN KEY (playerID) REFERENCES Player(playerID) ON DELETE SET NULL  -- אם שחקן נמחק, מזהה השחקן באירועים יקבל ערך NULL
);

