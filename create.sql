CHECK (jersey_number > 0 AND jersey_number < 100);
-- מספר החולצה חייב להיות בין 1 ל-99

ALTER TABLE Player
ADD CONSTRAINT positive_height
CHECK (height > 0);
-- הגובה חייב להיות חיובי

